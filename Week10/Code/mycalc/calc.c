float add_floats(float a, float b)
{
    return a + b;
}

float subtract_floats(float a, float b)
{
    return a - b;
}

float multiply_floats(float a, float b)
{
    return a * b;
}

float divide_floats(float a, float b)
{
    return a / b;
}
